from __future__ import annotations

import json
import os
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Mapping, Protocol


@dataclass(frozen=True, slots=True)
class CandidateSpaceManifest:
    """Backend-neutral description of one logical candidate space."""

    path: Path
    model_path: Path
    pcfg_rules_dir: Path
    total_points: int
    name: str

    @classmethod
    def load(cls, path: Path) -> "CandidateSpaceManifest":
        data = json.loads(path.read_text(encoding="utf-8"))
        base = path.parent
        return cls(
            path=path,
            model_path=_resolve_manifest_path(base, data["model_path"]),
            pcfg_rules_dir=_resolve_manifest_path(base, data["pcfg_rules_dir"]),
            total_points=int(data["total_points"]),
            name=str(data["name"]),
        )


@dataclass(frozen=True, slots=True)
class BenchmarkSpec:
    """Single benchmark request shared by all backend strategies."""

    candidate_space: CandidateSpaceManifest
    result_dir: Path
    limit: int
    hashes: tuple[str, ...]
    timeout_seconds: float
    python: str = sys.executable
    hash_algorithm: str = "md5"
    total_nodes: int = 2
    source_mode: str = "root"
    monitor_interval_seconds: float = 2.0
    max_rss_mib: float | None = None

    def __post_init__(self) -> None:
        if self.limit <= 0:
            raise ValueError("limit must be positive")
        if self.timeout_seconds <= 0.0:
            raise ValueError("timeout_seconds must be positive")
        if self.monitor_interval_seconds <= 0.0:
            raise ValueError("monitor_interval_seconds must be positive")
        if self.max_rss_mib is not None and self.max_rss_mib <= 0.0:
            raise ValueError("max_rss_mib must be positive")
        if self.source_mode not in {"root", "structure", "shard"}:
            raise ValueError("source_mode must be root, structure, or shard")


@dataclass(frozen=True, slots=True)
class BenchmarkCommand:
    """Concrete command generated by a backend strategy."""

    command: tuple[str, ...]
    env: Mapping[str, str] = field(default_factory=dict)
    monitor_process_tree: bool = True


class BenchmarkBackend(Protocol):
    """Build a command for one benchmark backend."""

    name: str

    def build(self, spec: BenchmarkSpec) -> BenchmarkCommand:
        """Return the command used to run this backend."""


@dataclass(frozen=True, slots=True)
class OursProtocolBackend:
    """CQDAGPCFG-Parallel local protocol backend."""

    name: str = "ours"

    def build(self, spec: BenchmarkSpec) -> BenchmarkCommand:
        work_dir = spec.result_dir / "work"
        model_cache_dir = spec.result_dir / "model_cache"
        command = [
            spec.python,
            "experiments/cqpcfg_experiment.py",
            "local",
            "--source-model-path",
            str(spec.candidate_space.model_path),
            "--work-dir",
            str(work_dir),
            "--limit",
            str(spec.limit),
            "--no-targets",
            "--hash-algorithm",
            spec.hash_algorithm,
            "--total-nodes",
            str(spec.total_nodes),
            "--source-mode",
            spec.source_mode,
            "--max-parallel-leases-per-node",
            str(max(2, spec.total_nodes - 1)),
            "--timeout-seconds",
            str(spec.timeout_seconds),
            "--ack-timeout-seconds",
            str(spec.timeout_seconds),
            "--fast-cracking-mode",
            "--model-cache-dir",
            str(model_cache_dir),
        ]
        for digest in spec.hashes:
            command.extend(["--decoy-hash", digest])
        env = {
            "CQPCFG_CONSUMER_BACKEND": "hashcat-auto",
            "CQPCFG_HASHCAT_DEVICE_TYPES": "1",
            "CQPCFG_HASHCAT_STDIN_VERIFY_ARTIFACTS": "0",
            "CQPCFG_HASHCAT_FINALIZE_TIMEOUT_SECONDS": str(spec.timeout_seconds),
        }
        return BenchmarkCommand(tuple(command), env=env)


@dataclass(frozen=True, slots=True)
class PcfgManagerBackend:
    """pcfg-manager server/client backend using the exported PCFG view."""

    pcfg_manager: Path
    port: int = 50151
    chunk_duration: str = "600s"
    chunk_start_size: int = 1_000_000
    client_count: int = 1
    max_rss_mib: float | None = None
    name: str = "pcfg-manager"

    def build(self, spec: BenchmarkSpec) -> BenchmarkCommand:
        command = [
            spec.python,
            "experiments/src/tools/run_pcfg_manager_hashcat.py",
            "--result-dir",
            str(spec.result_dir / "backend"),
            "--pcfg-manager",
            str(self.pcfg_manager),
            "--rules",
            str(spec.candidate_space.pcfg_rules_dir),
            "--limit",
            str(spec.limit),
            "--port",
            str(self.port),
            "--chunk-duration",
            self.chunk_duration,
            "--chunk-start-size",
            str(self.chunk_start_size),
            "--client-count",
            str(self.client_count),
        ]
        if self.max_rss_mib is not None:
            command.extend(["--max-rss-mib", str(self.max_rss_mib)])
        for digest in spec.hashes:
            command.extend(["--hash", digest])
        return BenchmarkCommand(tuple(command))


@dataclass(frozen=True, slots=True)
class FitcrackBackend:
    """Fitcrack PCFG backend configured with an already registered grammar."""

    cookie_path: Path
    grammar_id: int
    grammar_name: str
    grammar_keyspace: int
    poll_seconds: float = 10.0
    max_docker_memory_mib: float | None = None
    name: str = "fitcrack"

    def build(self, spec: BenchmarkSpec) -> BenchmarkCommand:
        command = [
            spec.python,
            "experiments/src/tools/run_fitcrack_pcfg_job.py",
            "--result-dir",
            str(spec.result_dir / "backend"),
            "--cookie-path",
            str(self.cookie_path),
            "--limit",
            str(spec.limit),
            "--name",
            f"{self.grammar_name}-{spec.limit}",
            "--timeout-seconds",
            str(spec.timeout_seconds),
            "--poll-seconds",
            str(self.poll_seconds),
            "--grammar-id",
            str(self.grammar_id),
            "--grammar-name",
            self.grammar_name,
            "--grammar-keyspace",
            str(self.grammar_keyspace),
        ]
        if self.max_docker_memory_mib is not None:
            command.extend(
                ["--max-docker-memory-mib", str(self.max_docker_memory_mib)],
            )
        for digest in spec.hashes:
            command.extend(["--hash", digest])
        return BenchmarkCommand(tuple(command), monitor_process_tree=False)


def command_with_monitor(
    *,
    command: BenchmarkCommand,
    result_dir: Path,
    interval_seconds: float,
    python: str,
    max_rss_mib: float | None = None,
) -> tuple[list[str], dict[str, str]]:
    env = os.environ.copy()
    env.update(command.env)
    if not command.monitor_process_tree:
        return list(command.command), env
    monitored = [
        python,
        "experiments/src/tools/monitor_command.py",
        "--samples-path",
        str(result_dir / "rss_samples.json"),
        "--summary-path",
        str(result_dir / "process_tree_summary.json"),
        "--interval-seconds",
        str(interval_seconds),
    ]
    if max_rss_mib is not None:
        monitored.extend(["--max-rss-mib", str(max_rss_mib)])
    monitored.extend(["--", *command.command])
    return monitored, env


def _resolve_manifest_path(base: Path, value: str) -> Path:
    path = Path(value)
    if path.is_absolute():
        return path
    cwd_relative = path.resolve()
    if cwd_relative.exists():
        return cwd_relative
    return (base / path).resolve()


